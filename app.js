let currentLanguage = localStorage.getItem('oldtech-language') || 'ru';
let activeCategory = 'All';
const cart = new Map();

const t = key => translations[currentLanguage][key] || key;

function applyLanguage() {
  document.documentElement.lang = currentLanguage;
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.dataset.i18n;
    if (translations[currentLanguage][key]) element.textContent = translations[currentLanguage][key];
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(element => {
    const key = element.dataset.i18nAria;
    if (translations[currentLanguage][key]) element.setAttribute('aria-label', translations[currentLanguage][key]);
  });
  document.querySelectorAll('[data-placeholder-ru]').forEach(input => {
    const placeholder = input.dataset[`placeholder${currentLanguage[0].toUpperCase()}${currentLanguage.slice(1)}`];
    if (placeholder) input.setAttribute('placeholder', placeholder);
  });
  document.querySelectorAll('.language-button').forEach(button => {
    button.classList.toggle('active', button.dataset.lang === currentLanguage);
  });
  renderFilters();
  renderProducts();
  renderCart();
}

function renderFilters() {
  const filters = document.getElementById('filters');
  filters.innerHTML = '';
  const filterItems = ['All', ...categories];
  filterItems.forEach(category => {
    const button = document.createElement('button');
    button.type = 'button';
    button.className = `filter-chip ${activeCategory === category ? 'active' : ''}`;
    button.textContent = category === 'All' ? t('filterAll') : category;
    button.addEventListener('click', () => {
      activeCategory = category;
      renderFilters();
      renderProducts();
    });
    filters.appendChild(button);
  });
}

function renderProducts() {
  const grid = document.getElementById('productGrid');
  const visibleProducts = activeCategory === 'All' ? products : products.filter(product => product.category === activeCategory);
  grid.innerHTML = '';

  visibleProducts.forEach(product => {
    const localized = product[currentLanguage];
    const count = cart.get(product.id) || 0;
    const card = document.createElement('article');
    card.className = `product-card reveal visible ${count > 0 ? 'has-count' : ''}`;
    card.innerHTML = `
      <div class="product-topline">
        <span class="product-category">${product.category}</span>
        <span class="product-count" aria-label="${count} ${t('units')}">${count}</span>
      </div>
      <h3 class="product-title">${localized.title}</h3>
      <p class="product-description">${localized.description}</p>
      <div class="product-meta">
        <span>${localized.maker}</span>
        <span>${localized.condition}</span>
      </div>
      <div class="product-specs">
        ${localized.specs.map(spec => `<span class="spec-pill">${spec}</span>`).join('')}
      </div>
      <div class="product-actions">
        <button class="product-action" type="button" data-add="${product.id}">${t('addToRequest')}</button>
        <div class="quantity-controls">
          <button class="quantity-button" type="button" data-remove="${product.id}" aria-label="${t('removeOne')}">−</button>
          <button class="quantity-button" type="button" data-add="${product.id}" aria-label="${t('increaseOne')}">+</button>
        </div>
      </div>
    `;
    grid.appendChild(card);
  });
}

function addToCart(productId) {
  cart.set(productId, (cart.get(productId) || 0) + 1);
  updateCartViews();
}

function removeFromCart(productId) {
  const current = cart.get(productId) || 0;
  if (current <= 1) cart.delete(productId);
  else cart.set(productId, current - 1);
  updateCartViews();
}

function updateCartViews() {
  renderProducts();
  renderCart();
}

function renderCart() {
  const total = [...cart.values()].reduce((sum, value) => sum + value, 0);
  document.getElementById('cartTotal').textContent = total;
  const summary = document.getElementById('cartSummary');
  summary.innerHTML = '';

  if (total === 0) {
    const empty = document.createElement('div');
    empty.className = 'empty-cart';
    empty.textContent = t('emptyCart');
    summary.appendChild(empty);
    return;
  }

  [...cart.entries()].forEach(([id, count]) => {
    const product = products.find(item => item.id === id);
    if (!product) return;
    const line = document.createElement('div');
    line.className = 'cart-line';
    line.innerHTML = `<strong>${product[currentLanguage].title}</strong><small>${count} ${t('units')}</small>`;
    summary.appendChild(line);
  });
}

function initEvents() {
  document.querySelectorAll('.language-button').forEach(button => {
    button.addEventListener('click', () => {
      currentLanguage = button.dataset.lang;
      localStorage.setItem('oldtech-language', currentLanguage);
      applyLanguage();
    });
  });

  document.getElementById('productGrid').addEventListener('click', event => {
    const addId = event.target.closest('[data-add]')?.dataset.add;
    const removeId = event.target.closest('[data-remove]')?.dataset.remove;
    if (addId) addToCart(addId);
    if (removeId) removeFromCart(removeId);
  });

  const navToggle = document.querySelector('.nav-toggle');
  const navShell = document.querySelector('.nav-shell');
  navToggle.addEventListener('click', () => {
    const isOpen = navShell.classList.toggle('open');
    navToggle.setAttribute('aria-expanded', String(isOpen));
  });

  document.querySelectorAll('.nav-links a, .nav-actions a').forEach(link => {
    link.addEventListener('click', () => {
      navShell.classList.remove('open');
      navToggle.setAttribute('aria-expanded', 'false');
    });
  });

  const form = document.getElementById('requestForm');
  form.addEventListener('submit', event => {
    event.preventDefault();
    const status = document.getElementById('formStatus');
    if (!form.checkValidity()) {
      form.reportValidity();
      return;
    }
    status.textContent = t('successMessage');
    form.reset();
  });
}

function initReveal() {
  const elements = document.querySelectorAll('.reveal');
  if (!('IntersectionObserver' in window)) {
    elements.forEach(element => element.classList.add('visible'));
    return;
  }

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: .12 });

  elements.forEach(element => observer.observe(element));
}

initEvents();
applyLanguage();
initReveal();
